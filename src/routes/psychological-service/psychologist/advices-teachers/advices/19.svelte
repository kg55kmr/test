<p>
  Тобто агресор отримає підтвердження того, що він домігся того, заради чого і
  здійснював цькування. А жертва отримає чергове, та ще й привселюдне,
  приниження. Бесіди щодо з’ясування обставин, виховні бесіди тощо треба
  проводити або в дуже вузькому колі людей, або наодинці. Важливо залучити дітей
  до обговорення цієї теми, разом виробити план дій щодо подолання булінгу у
  класі.
</p>
<p>
  Провести батьківські збори, присвячені темі цькування. Надати батькам
  інформацію про булінг (причини виникнення, дійові особи, сигнали про
  наявність, наслідки та інше), що допоможе їм попереджати або визначати явище
  цькування.
</p>
<p>
  Коли гостра стадія процесу минула, впроваджуються кроки, що забезпечать у
  колективі атмосферу поваги та толерантності, упорядкують поведінку дітей.
</p>
<p>
  Важливо розробити, можливо разом з учнями, класну збірку правил або Кодекс
  поведінки, що буде визначати прийнятні та недопустимі види поведінки у класі
  та школі.Корисним для учнів буде періодично обговорювати та розбирати, чи
  вдається їм дотримуватись цього Кодексу, що заважає або допомагає і т. д.
</p>
<p>
  Залучення всього колективу до якоїсь спільної справи, що буде об’єднувати
  всіх, згуртовувати. Важливо підібрати таку справу, до якої були би залучені
  УСІ учні. Або це може бути декілька справ чи заходів, де у різних сферах
  будуть брати участь УСІ! Щоб кожен учень зміг відчути себе корисним для інших,
  мав визнання у чомусь своєму, мав змогу показати себе.
</p>
<p>
  Проведення разом з психологом або соціальним педагогом різноманітних вправ та
  ігор для формування у колективі почуття згуртованості, доброзичливості,
  толерантності, довіри, взаємоповаги тощо.
</p>
<p>
  Як бачите, протидіяти булінгу складно та довго. Це потребує великих зусиль,
  займає багато часу, а результат можна побачити не одразу. І подолавши наслідки
  конкретної ситуації, не можна думати, що цькування не повториться.
</p>
<p>
  Головне у боротьбі з булінгом – постійна профілактична діяльність, швидке
  реагування та подолання найменших проявів та зародків цькування.
</p>
