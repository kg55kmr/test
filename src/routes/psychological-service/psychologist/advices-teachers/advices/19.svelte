<p>
  На що треба звернути увагу вчителю, що може бути передвісниками або маркерами
  цькування у класі:
</p>
<ul>
  <li>занадто підвищена емоційна атмосфера у класі;</li>
  <li>
    ви помічаєте, що діти припиняють спілкуватися та замовкають при появі поряд
    вчителя або дорослого;
  </li>
  <li>спостерігається відокремлення окремих учнів від решти класу;</li>
  <li>у класі сталося «перегрупування» дружніх кіл;</li>
  <li>
    кардинальна зміна поведінки окремої дитини – як пригнічена, сумна, закрита,
    так і навпаки – занадто піднесена, жвава, агресивна;
  </li>
  <li>раптове та нехарактерне зниження шкільної успішності окремої дитини;</li>
  <li>
    часті хвороби та/або прогули окремої дитини, не притаманні їй до цього часу.
  </li>
</ul>

<p>
  <b>
    ЯКЩО ВИ ПОМІТИЛИ ЯКІСЬ З ЦИХ ОЗНАК ТА ЗАПІДОЗРИЛИ НЕГАРАЗДИ У ДИТЯЧИХ
    ВІДНОСИНАХ, ВАРТО ЗВЕРНУТИСЯ ДО ШКІЛЬНОГО ПСИХОЛОГА ЗА ДОПОМОГОЮ. ПСИХОЛОГИ
    МАЮТЬ У СВОЄМУ АРСЕНАЛІ МЕТОДИКИ, ЯКІ ДОПОМОЖУТЬ ВИЯВИТИ ЙМОВІРНИХ АБО ВЖЕ
    ІСНУЮЧИХ УЧАСНИКІВ ЦЬКУВАННЯ.
  </b>
</p>

<p>Що важливо пам’ятати, працюючи з булінгом:</p>
<ul>
  <li>
    Цькування – явище групове, одна з форм поведінки групи, тож працювати щодо
    його подолання треба з усіма членами групи. Окрема робота (виховні бесіди,
    каральні санкції, корекція з психологом тощо) тільки з жертвою або тільки з
    агресором призведе до тимчасового результату, але цькування не зупинить.
  </li>
  <li>
    Група, яка згуртувалася навколо загальної цікавої мети, не буде
    цькувати.Гарний приклад – секції у дитячих будинках творчості, компанії
    однодумців. А от група, яку створили штучно, у якої немає спільної мети, що
    об’єднує, але є потреба у згуртуванні (а шкільні класи є саме такими
    групами), обов’язково знайде, проти чого або, скоріш, проти КОГО
    об’єднатися.
  </li>
  <li>
    Діти ніколи не можуть впоратися з булінгом самотужки, це не «пройде саме»! У
    цьому процесі вкрай необхідне втручання стороннього дорослого – і не одного.
  </li>
  <li>
    Важливо розрізняти непопулярність та цькування.Непопулярна дитина може бути
    без друзів у класі, мало спілкуватися та не «лізти у перші ряди» у
    громадській діяльності. Цькування може маскуватися під непопулярність, але
    коли дитина потерпає від насильства, психологічного або фізичного, сумнівів
    не має бути.
  </li>
</ul>

<p>Що потрібно робити, коли виявлено цькування:</p>
<ul>
  <li>
    Припинити процес насильства та достеменно визначити, що трапилося, коли, де,
    хто учасники, свідки, скільки часу триває тощо.
  </li>
  <li>
    Повідомити про ситуацію дирекцію школи та батьків учасників булінгу.
    По-перше, ситуація серйозна, до неї залучено багато дійових осіб, існують
    ймовірні тяжкі наслідки, тож вирішувати цю проблему повинні усі разом. А
    по-друге, нещодавно прийнятий закон України № 8584 щодо подолання булінгу
    передбачає адміністративну відповідальність усіх дієздатних осіб, які брали
    участь у цькуванні або які нічого не зробили для його припинення.
  </li>
  <li>
    Звернутися по допомогу до інших спеціалістів – психолога, соціального
    робітника, медіатора, експерта та інше.
  </li>
  <li>
    Разом з батьками, дирекцією школи, іншими спеціалістами (психолог,
    соціальний робітник, медіатор тощо) визначити та реалізувати заходи з
    підтримки постраждалого – надання медичної та психологічної допомоги,
    тимчасове невідвідування шкільних занять, переведення до іншого класу та
    інше.
  </li>
  <li>
    Разом з дирекцією, батьками дитини-агресора та іншими спеціалістами
    (психолог, соціальний робітник, медіатор) визначити і застосувати
    дисциплінарні та виховні заходи щодо дитини-агресора. Це може бути бесіда,
    зауваження, догана, подальше спостереження та надання психологічної
    допомоги.
  </li>
  <li>Провести з класом бесіду щодо цькування у колективі.</li>
</ul>

<p>
  <b>
    ПРИВСЕЛЮДНО НЕ МОЖНА НАЗИВАТИ СКРИВДЖЕНУ ДИТИНУ ЖЕРТВОЮ, А ЇЇ КРИВДНИКА –
    АГРЕСОРОМ! ЦЕ ТІЛЬКИ ПІДТВЕРДИТЬ «ПРАВОТУ» СИТУАЦІЇ, РОЛЕЙ ЇЇ УЧАСНИКІВ ТА
    ЇХ ДІЙ.
