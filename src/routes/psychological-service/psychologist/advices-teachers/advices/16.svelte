  впродовж 20 хв. відразу ж після пробудження, ще до ранкових процедур та кави.
  Важливо записувати все, що приходить в голову, постарайтеся не зупинятися, не
  починати обмірковувати якусь одну думку, а просто безперервно писати. Не
  потрібно перечитувати і давати сторінки іншим. Таким чином ви зливаєте все
  зайве: занепокоєння, страхи, негатив, агресію.
</p>

