<h2>ПСИХОГІГІЄНА ПРОФЕСІЙНОЇ ДІЯЛЬНОСТІ ПЕДАГОГА В УМОВАХ КАРАНТИНУ</h2>

<p><b>Бути «ресурсним дорослим».</b></p>

<p>
  Надати ефективну й своєчасну підтримку та допомогу своїй родині, близьким,
  учням, колегам можливо тільки за умови, якщо ти сам знаходишся в «ресурсному
  стані» за всіма показниками професійного здоров’я, як фізичного, так і
  психологічного, піклуєшся про власне здоров’я та благополуччя. У цьому сенсі
  головним завданням для педагога повинно стати зниження рівня тривоги, страхів,
  паніки та підтримка емоційної рівноваги. Дбайте про себе, щоб мати змогу
  піклуватися про інших! Намагайтесь організувати особистий простір; домовтеся,
  щоб родина поважала та не порушувала психологічні межі. Виділяйте час «тільки
  для себе». У цей час робіть те, що дарує вам радість, спокій, задоволення,
  відновлює ваші ресурси (хобі, медитація, творчість, догляд за собою тощо).
</p>

<p>
  <b>Позитивне мислення.</b> Концентруйтесь на позитиві. Для стимулювання позитивного
  мислення доцільно використовувати надихаючі фото та позитивні афірмації (фрази,
  речення). З давніх часів знайома кожному фраза із притчі про перстень Соломона
  «Це мине. І це теж мине». Відомий нам вислів Марка Аврелія «Якщо ти не можеш змінити
  обставини, зміни своє ставлення до них» тощо.
</p>

<p>
  Знайдіть для себе способи відчувати позитивні емоції. Переглядайте комедії,
  прослуховуйте музику, займайтеся улюбленими справами.
</p>

<p>
  <b>«Інформаційна дієта».</b> Інформаційний стрес буває двох типів: інформаційна
  недостатність та інформаційна надмірність. Обидва типи інформаційного стресу негативно
  впливають на психологічне здоров’я людини. Отже, під час пандемії слід зважено
  користуватись інформаційними ресурсами: черпати інформацію з авторитетних і достовірних
  джерел і в жодному разі не «накручувати» себе зайвою емоційно-руйнівною інформацією
  з випусків теленовин або соціальних мереж. Дозуйте кількість інформації на день,
  аналізуйте, мисліть критично. Відокремте те, що знаходиться під вашим контролем,
  і те, на що ви вплинути не можете.
</p>

<p>
  <b>Тайм-менеджмент.</b> Одним із значних недоліків дистанційної роботи є те, що
  не кожна людина має розвинену силу волі й може організувати себе на виконання професійних
  завдань. Іноді люди, особливо трудоголіки, втрачають відчуття часу та не дотримуються
  режиму праці й відпочинку.
</p>

<p>
  В умовах карантину тайм-менеджмент, раціональний розподіл робочого часу та
  відпочинку стає однією з організаційних умов психогігієни професійної
  діяльності. Плануйте ваш день, максимально зберігаючи звичний режим
  професійної діяльності. Криза відчувається тим більше, чим більше порушений
  звичний уклад життя людини, її традиційний план, розклад. Організм
  «хвилюється», подає сигнал у мозок «Щось не те відбувається», починається
  стрес.
</p>

<p>
  <b>Розподіл пріоритетів між завданнями.</b> Ефективним прийомом тайм-менеджменту
  в умовах карантину може стати матриця Ейзенхауера. За допомогою матриці визначте
  й усвідомте, що у вашому житті є важливим, а що не має важливого значення, та вибудовуйте
