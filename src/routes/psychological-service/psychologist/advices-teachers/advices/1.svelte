  вона важлива? що станеться, якщо я просто зараз її не виконаю? що станеться,
  якщо я її взагалі не виконаю?). Спробуйте створювати таку матрицю для
  визначення пріоритетів щодня. Ідеально, якщо квадрат «Важливі термінові
  справи» буде у вас порожнім: а це цілком реально, якщо ви частіше будете
  займатися справами квадрата «Важливі нетермінові справи». Дбайте про
  задоволення свої першочергових потреб і потреб ваших близьких! Пам’ятайте про
  цінності, що надають сенс вашому життю.
</p>

<p>
  <b>Режим.</b> Дотримуйтесь питного режиму (не менше 1,5–2 літрів чистої питної
  води за добу), режиму праці та відпочинку. Пам’ятайте, що в процесі роботи в школі
  у вас є перерви, обід, час на спілкування з колегами. Застосовуйте прийоми командної
  роботи. У разі наявних спільних можливостей та бажання, ви можете домовлятись із
  колегами про спільне проведення онлайн-перерви, обговорення проблем та труднощів,
  можливих рішень та порад. Не відмовляйтесь від спілкування. Комунікація, соціальні
  зв’язки – це важлива частина життя, і педагоги не є виключенням. Влаштовуйте онлайн-вечірки
  або онлайн-посиденьки з колегами та друзями.
</p>

<p>
  <b>Перерва для відпочинку очей.</b> Зараз йде активна посилена робота з комп’ютерами,
  різноманітними гаджетами, тому рекомендуємо кожні 2 години роботи влаштовувати
  собі 10–15-хвилинну перерву для відпочинку очей. Роздрукуйте комплекси вправ для
  очей та з метою нагадування тримайте їх поряд на робочому столі.
</p>

<p>
  <b>Враховуйте власні біоритми.</b> Окремі види діяльності (організаційна, самоосвіта
  тощо), можна зараз спланувати у визначені вами години. У кожної людини протягом
  дня є власні біоритми, періоди підвищеної ефективної діяльності. Організм відчуває,
  коли в нього пік активності, потрібно до нього лише прислухатися. Протягом таких
  активних періодів сила, енергія, працездатність найефективніша. Тому можна зробити
  багато за короткий проміжок часу.
</p>

<p>
  <b>Рухова активність.</b> На період зменшення життєвого темпу під час карантину
  знаходьте час для рухової активності. Обов’язково додайте до свого розкладу провітрювання
  приміщень, прогулянки на свіжому повітрі в безлюдних місцях або перебування на
  балконі. Відсутність рухової активності може призвести до гіподинамії. Як наслідок,
  знижується вміст кисню в крові, кровопостачання в різні органи, сповільнюються
  процеси обміну речовин. Допоможіть у цей час своєму тілу: робіть зарядку, фізичні
  вправи; займайтеся на онлайн-курсах йоги чи медитації, спробуйте самомасаж, контрастний
  душ та інші засоби, що допоможуть вам розслабитись, а потім додадуть фізичного
  тонусу і бадьорого настрою. Усе це дуже корисно для імунітету, здоров’я. До того
  ж, це забезпечить відчуття продуктивності вашого життя, що теж важливо в кризових
  ситуаціях.
</p>

<p>
  <b>Увага до свого стану</b> (фізичного, емоційного, психологічного). Своєчасно
  звертайтеся за потрібною фаховою допомогою, якщо відчули тривожні для вас емоційні
  проблеми. Застосовуйте методи самодіагностики та саморегуляції емоційного стану.
  Це допоможе вам вчасно підтримати свій психологічний комфорт.
</p>

<p>
  Чи ви «в нормі»? Чи потрібна вам допомога? Приділяйте увагу своїм почуттям.
  Намагайтеся не блокувати їх. Важливо розуміти, що з вами відбувається. Не
  тримайте все в собі. Якщо можна, діліться своїми думками та емоціями з людьми,
  які здатні вас підтримати. Зверніться за допомогою до працівників
  психологічної служби, які працюють у вашому закладі, або до тих, кому ви
  довіряєте.
</p>

<p>
  Розмова з близькими чи фахівцем допоможе заспокоїтися та подивитися на
  ситуацію під іншим кутом. Відчувати страх та тривогу в надзвичайних ситуаціях
  не соромно. Пам’ятайте, у вас є право хвилюватись, до того ж хвилюватись щодо
  здоров’я та життя, свого та рідних, – природно, але лише усвідомлюючи це, ви
  можете розв’язати цю проблему.
</p>
