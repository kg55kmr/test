<p><b>Метод ключових слів</b></p>

<p>Ключові слова - найважливіші в кожному абзаці.</p>
<p>
  Ключове слово має сприяти відтворенню відповідного абзацу. Згадуючи ключові
  слова, ми одразу згадуємо весь абзац.
</p>
<p>
  Читаючи абзац, вибери для нього одне-два ключових слова. Після вибору ключових
  слів запиши їх у тій послідовності, яка потрібна для виконання завдання.
</p>
<p>
  До кожного ключового слова постав запитан­ня, що дасть змогу зрозуміти, як
  воно пов'язане з відповідним розділом тексту. Обміркуй і спробуй зрозуміти цей
  взаємозв'язок.
</p>
<p>З'єднай два сусідніх ключових слова за допо­могою запитань.</p>
<p>
  Після поєднання кожного ключового слова зі своїм розділом тексту і з наступним
  ключовим словом утвориться ланцюжок. Запиши цей ланцюжок і спробуй його
  вивчити . Перекажи текст спираючись на цей ланцюжок.
</p>

<p><b>Метод 5-П</b></p>
<ul>
  <li>1-П - Переглянь текст швидко.</li>
  <li>2-П - Придумай до нього запитання.</li>
  <li>3-П - Познач олівцем найважливіші місця.</li>
  <li>4-П - Перекажи текст.</li>
  <li>5-П - Переглянь текст повторно.</li>
</ul>

