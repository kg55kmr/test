  для всієї людської спільноти. У суспільстві ми повинні з особливим співчуттям
  і підтримкою ставитися до людей з ВІЛ, адже найчастіше вони відчувають себе
  самотніми у боротьбі зі страшною недугою.
</p>
<p>
  Сьогодні вже немає людини, яка б не чула про ВІЛ, СНІД. Багато людей в Україні
  вважають себе обізнаними з цього питання. Проте більшість молодих людей
  вважають, що ця проблема – це щось далеке, що вона їх не стосується і не буде
  стосуватися в майбутньому. Факти доводять протилежне – кожного року фіксують
  все більше випадків захворювань на СНІД.Незважаючи на успіхи медицини,
  ВІЛ/СНІД все ще залишається невиліковною хворобою.
</p>
<p>
  Соціальні контакти з ВІЛ-позитивними і сьогодні супроводжуються міфами та
  безпідставними страхами інфікування.
</p>

<p>
  Допоки не існує вакцини чи можливості лікування, основними завданнями
