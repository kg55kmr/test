<p><b>Перебіг ВІЛ-інфекції</b></p>

<p>
  Імунна система дорослої ВІЛ-інфікованої людини протягом перших декількох років
  після інфікування, незважаючи на шкоду, якої завдає вірус, працює порівняно
  добре. В цей час немає практично ніяких симптомів захворювання, за винятком
  збільшення лімфатичних вузлів та легких захворювань шкіри. Більшість дорослих
  людей на цій стадії захворювання не підозрюють, що вони інфіковані ВІЛ. Тільки
  через декілька років, коли імунна система стає слабкішою, з’являються прояви
  захворювання, які дають підставу запідозрити ВІЛ-інфекцію.
</p>
<p>
  Тривалий період, коли люди, які живуть з ВІЛ, почуваються добре, називають
  латентною, або прихованою, фазою захворювання. В дорослих людей вона може
  тривати до 5–8 років, а іноді й довше.
</p>
<p>
  Прогресування ВІЛ-інфекції, окрім опортуністичних інфекцій, може
  супроводжуватися значною втратою маси тіла (виснаженням), ураженням
  центральної нервової системи (ВІЛ-енцефалопатією), розвитком пухлин. Стадію
  захворювання, коли імунна система ВІЛ-позитивної людини є зруйнованою,
  називають СНІДом, а стани, які розвиваються на цій стадії, –
  СНІД-індикаторними.
</p>

<p><b>Відповіді на часті запитання</b></p>
<ol>
  <li>
    Чи існує можливість інфікуватися ВІЛ побутовим шляхом?
    <p>
      <i>
        ​Ні, інфікуватися ВІЛ побутовим шляхом неможливо. Всі види побутових
        контактів із ВІЛ-позитивними людьми не можуть призвести до інфікування
        ВІЛ. ВІЛ-інфекція не може бути перешкодою спільному проживанню,
        використанню предметів побуту, посуду та ін. Всі види побутових
        контактів – рукостискання, дотики, обійми є цілком безпечними.
      </i>
    </p>
  </li>
  <li>
    Чи означають терміни «ВІЛ» і «СНІД» одне й те ж?
    <p>
      <i>
        Ні, терміни «ВІЛ» і «СНІД» не є синонімами. ВІЛ – це вірус імунодефіциту
        людини, він викликає ВІЛ-інфекцію. СНІД означає синдром набутого
        імунодефіциту – це одна зі стадій ВІЛ-інфекції. В людей, які живуть з
        ВІЛ, не на всіх стадіях захворювання спостерігається порушення стану
        імунної системи, тобто є імунодефіцит. Тому не в усіх ВІЛ-інфікованих
        розвивається СНІД. Сучасне лікування ВІЛ-інфекції дозволяє запобігти
        руйнуванню імунної системи і розвитку СНІДу або сприяє відновленню
        імунітету. У випадках відновлення імунітету в людини зникають симптоми
        СНІДу, але вона залишається ВІЛ-інфікованою.
      </i>
    </p>
  </li>
  <li>
    Чи всі діти ВІЛ-позитивних матерів інфікуються ВІЛ?
    <p>
      <i>
        Ні, не всі діти, народжені ВІЛ-інфікованими матерями, інфікуються ВІЛ.
        За умови відсутності профілактики передачі ВІЛ від матері до дитини
        ризик інфікування дитини складає 35 %. Програма профілактики передачі
        ВІЛ від матері до дитини дозволяє суттєво знизити цей ризик.
        Стратегічним завданням цієї програми є зниження ризику передачі ВІЛ від
        матері до дитини до 1–2 %, що надасть можливість майже всім
        ВІЛ-інфікованим жінкам мати здорових дітей.
      </i>
    </p>
  </li>
  <li>
    Чи існує вакцина, що може запобігти інфікуванню ВІЛ?
    <p>
      <i>
        Ні, на жаль, не існує. Вчені багатьох країн світу працюють над
        створенням такої вакцини. Однак ефективність наявних наукових розробок
        ще не доведено. Впровадження таких вакцин у практичну медицину є
        питанням майбутнього, а не сьогодення.
      </i>
    </p>
  </li>
</ol>
