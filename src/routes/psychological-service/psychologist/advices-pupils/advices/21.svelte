</p>
<ul>
  <li>розповсюдження інформації;</li>
  <li>захист себе та інших;</li>
  <li>розвиток поведінкових навичок.</li>
</ul>

<p>
  ВІЛ-інфекція – це одне з хронічних вірусних захворювань, викликане вірусом
  імунодефіциту людини (ВІЛ), що призводить до глибокого порушення імунної
  системи, яке визначають медичним терміном «імунодефіцит».
</p>
<p>
  Коли ВІЛ потрапляє в організм людини, він проникає у клітини імунної системи
  та руйнує їх. Перебуваючи в клітинах, вірус «маскується», тобто стає невидимим
  для імунної системи. Мішенню для ВІЛ в організмі людини є лімфоцити, які мають
  на своїй поверхні CD4-рецептори. Такі лімфоцити називаються CD4-лімфоцити.
  Стійкість ВІЛ у зовнішньому середовищі:
</p>
<ul>
  <li>
    В сухій плямі крові, яка містить ВІЛ, активність вірусу зникає протягом
    кількох діб.
  </li>
  <li>
    У великій кількості крові при температурі 23–27 °С активність вірусу зникає
    через 15 діб.
  </li>
  <li>
    У продуктах крові, призначених для переливання, вірус може залишатися
    активним протягом багатьох років, у замороженій сироватці крові – до 10
    років.
  </li>
  <li>
    ВІЛ швидко гине при використанні дезінфекційних засобів, ультрафіолетового
    випромінювання. При нагріванні вище 56 °С вірус втрачає активність через 30
    хвилин.
  </li>
</ul>

