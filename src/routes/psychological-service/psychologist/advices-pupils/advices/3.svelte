  <li>Не виявляти до співрозмовника зверхності;</li>
  <li>Щиро цікавитися співрозмовником, тим, що він говорить;</li>
  <li>Враховувати поведінку та термінологію людини, з якою говорите;</li>
  <li>Нападами та лайкою людей не переконаєте;</li>
  <li>
    Для того, щоб переконати когось, ттеба"знайти необхідні аргументи, правильно
