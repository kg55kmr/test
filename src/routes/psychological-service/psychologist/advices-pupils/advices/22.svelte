  першу чергу, спробує з 'ясувати, чи все добре з самоповагою у клієнта. Якщо
  діагностика виявить низьку самооцінку, насамперед, потрібно працювати з нею.
  Адже часто саме це основна причина всіх проблем.
</p>

<h2>Важливі складові самооцінки</h2>
<p>
  Головним аспектом виступає емоційний фактор. Тобто, як людина відчуває собі в
  соціумі. А також відіграє чималу роль його уявлення про себе і про свої
  можливості. Крім того, це ще й розуміння індивідуумом своїх бажань і
  можливостей.
</p>
<p>
  Рівень знань і компетентність у потрібній галузі. Цим володіє людина, яка
  розуміє, що вона робить і навіщо. А головне - як це потрібно робити. Тому він
  часто має позитивні результати від виконаної роботи. Це формула проста: знаю,
  роблю, отримую успішний результат. Саме досягнення та успіхи допомагають
  почуватися добре, і навіть піднесено. А значить, мати самоповагу.
</p>
<p>
  Особисті взаємини з друзями, колегами та коханими людьми. Індивідуум, який має
  адекватну самооцінку, поважає себе і може налагодити нормальні, здорові
  відносини з оточуючими людьми. Він не буде затиснутий у спілкуванні з
  протилежною підлогою. А якщо важливі люди приймають його і цінують, то він
  насолоджується самоповагою.
</p>
<p>
  І навпаки відбувається з людиною, у якої низька самооцінка. Адже спілкуватися
  йому важко, а часто на тлі цього виникають комплекси і страхи. Стосунки з
  протилежною статтю можуть закінчитися, навіть ще й не почавшись. Адже часто
  страх заважає перейти до активних дій і будувати відносини.
</p>
